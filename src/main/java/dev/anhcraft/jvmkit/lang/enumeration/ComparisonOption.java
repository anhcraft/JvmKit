package dev.anhcraft.jvmkit.lang.enumeration;

/**
 * Extra options for the comparison process.
 */
public enum ComparisonOption {
    /**
     * Enables case-insensitive comparison for characters/strings.
     */
    CASE_INSENSITIVE,

    /**
     * Ignores the decimal part when compares two numbers.
     */
    IGNORE_DECIMAL
}
